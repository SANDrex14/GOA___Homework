# 1. for loop
for i in range(5):
    print("გამეორება:", i)

# 2. while loop
count = 0
while count < 5:
    print("მიმდინარე რიცხვი:", count)
    count += 1

# 3. for loop-ით სიაში ელემენტების ბეჭდვა
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print("ბოჭკო:", fruit)

# 4. while loop-ით რიცხვების გაწერა
num = 1
while num <= 5:
    print("რიცხვი:", num)
    num += 1

# 5. for loop-ით გამრავლების სია
for i in range(1, 11):
    print("2 *", i, "=", 2 * i)

# 6. while loop-ით რიცხვების გამოტანა
i = 0
while i <= 10:
    print(i)
    i += 1

